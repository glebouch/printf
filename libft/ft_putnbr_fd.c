#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int min;

	min = -2147483648;
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
		if (n == -min)
		{
			write(fd, "2147483648", 10);
			return ;
		}
	}
	if ((n / 10) != 0)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}
